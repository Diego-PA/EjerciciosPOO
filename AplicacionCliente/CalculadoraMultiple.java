public class CalculadoraMultiple {

    private float fNumero, fResultado;

    public CalculadoraMultiple(){
        this.fNumero = 0;
        this.fResultado = 0;
    }

    public void setNumero(float fNumero){
        this.fNumero = fNumero;
    }

    public void setResultado(float fResultado){
        this.fResultado = fResultado;
    }

    public float getNumero(){
        return fNumero;
    }

    public float getResultado(){
        return fResultado;
    }

}
