public class MiFecha{
	
	private int eDia;
	private int eMes;
	private int eAnio;

	public setDia(int dia){
		this.eDia = dia;
	}
	public setMes(int mes){
		this.eMes = mes;
	}
	public setAnio(int anio){
		eAnio = anio;
	}

	public int getDia(){
		return this.eDia;
	}
	public int getMes(){
		return this.eMes;
	}
	public int getAnio(){
		return this.eAnio;
	}
}
